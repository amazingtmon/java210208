       ,SUM(O.QTY_NU*PRICE_NU)||'원' 
    FROM T_ORDERBASKET O
      ,(SELECT ROWNUM RNO FROM TEMP WHERE ROWNUM<3) DUMY
GROUP BY DECODE(DUMY.RNO,1,O.INDATE_VC,2,'총계')
ORDER BY DECODE(DUMY.RNO,1,O.INDATE_VC,2,'총계')                                   


