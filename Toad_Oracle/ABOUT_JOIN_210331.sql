카타시안의 곱 - 데이터를 복제하는 용도로 사용된다.
소계나 총계관련 함수를 지원하지 않는 경우 카타시안의 곱을 활용하여 집계처리를 해야한다.

SELECT DEPT, COLL FROM TEST11
,(SELECT ROWNUM RNO REMO TEMP WHERE ROWNUM<3)

SELECT T1.DEPT, T1.COLL
  FROM TEST11 T1
       ,(SELECT ROWNUM RNO FROM TEMP WHERE ROWNUM<3) T2 --인라인뷰
       
SELECT T1.DEPT, T1.COLL
      ,DECODE(RNO,1,'1,2학년',2,'3,4학년') AS "학년"
      ,DECODE(RNO,1,FRE,2,JUN) AS "학년"
  FROM TEST11 T1
       ,(SELECT ROWNUM RNO FROM TEMP WHERE ROWNUM<3) T2 --인라인뷰

SELECT T1.DEPT, T1.COLL
      ,DECODE(RNO,1,'1,2학년',2,'3,4학년') AS "학년"
      ,DECODE(RNO,1,FRE,2,JUN) AS "1,2학년"
      ,DECODE(RNO,1,SUP,2,SEN) AS "3,4학년"
  FROM TEST11 T1
       ,(SELECT ROWNUM RNO FROM TEMP WHERE ROWNUM<3) T2           
ORDER BY DEPT DESC, "학년"
       
